#include "push_swap.h"

void	push(t_stack **src, t_stack **dest)
{
	t_stack	*head_src;

	if (*src)
	{
		head_src = *src;
		*src = head_src->next;
		head_src->next = *dest;
		*dest = head_src;
	}
}

void	pa(t_stack **a, t_stack **b)
{
	push(b, a);
	ft_printf("pa\n");
}

void	pb(t_stack **a, t_stack **b)
{
	push(a, b);
	ft_printf("pb\n");
}
